/**
 * P5 Capability Spike Plugin
 *
 * 基于 cline/cline/sdk/examples/plugins/custom-compaction.ts 改造
 * 验证目标：registerMessageBuilder 触发 compact 时同步写出 handoff.md + 追加 index.jsonl
 * 附加验证：session_start hook（#6）
 *
 * 改造点：
 * 1. compact 触发时写出 handoff.md（compact summary → handoff 文件）
 * 2. compact 触发时追加 index.jsonl（compact 事件记录）
 * 3. 添加 beforeRun hook 尝试捕获 session 启动（#6）
 *
 * 母本来源：https://github.com/cline/cline/blob/main/sdk/examples/plugins/custom-compaction.ts
 */
import type { AgentPlugin, Message, ToolResultContent } from "@cline/core";
import { estimateTokens as estimateTokensFromChars } from "@cline/shared";
import * as fs from "node:fs";
import * as path from "node:path";

const MAX_INPUT_TOKENS = 120_000;
const COMPACT_AT_RATIO = 0.75;
const PRESERVE_RECENT_TOKENS = 24_000;
const SUMMARY_PREVIEW_CHARS = 800;

// Spike 改造：handoff 输出目录（相对于 plugin 运行时的 cwd）
const HANDOFF_DIR = "handoff";
const HANDOFF_FILE = "handoff.md";
const INDEX_FILE = "index.jsonl";

function estimateTokens(text: string): number {
	return estimateTokensFromChars(text.length);
}

function preview(text: string, limit = SUMMARY_PREVIEW_CHARS): string {
	if (text.length <= limit) {
		return text.trim();
	}
	return `${text.slice(0, limit).trim()}\n...[${text.length - limit} more chars summarized]`;
}

function stringifyContent(content: ToolResultContent["content"]): string {
	return typeof content === "string" ? content : JSON.stringify(content);
}

function serializeMessage(message: Message): string {
	if (typeof message.content === "string") {
		return `[${message.role}]: ${message.content}`;
	}
	const lines: string[] = [];
	for (const block of message.content) {
		switch (block.type) {
			case "text":
				lines.push(`[${message.role}]: ${block.text ?? ""}`);
				break;
			case "thinking":
				lines.push(
					`[assistant thinking]: ${preview(block.thinking ?? "", 300)}`,
				);
				break;
			case "tool_use":
				lines.push(
					`[assistant tool call]: ${block.name ?? "tool"}(${JSON.stringify(block.input ?? {})})`,
				);
				break;
			case "tool_result":
				lines.push(
					`[tool result ${block.tool_use_id ?? "unknown"}]: ${preview(stringifyContent(block.content), 500)}`,
				);
				break;
			case "file":
				lines.push(
					`[file ${block.path ?? "unknown"}]: ${preview(String(block.content ?? ""), 500)}`,
				);
				break;
			default:
				lines.push(`[${message.role} ${block.type} block]`);
		}
	}
	return lines.join("\n");
}

function estimateMessageTokens(message: Message): number {
	return estimateTokens(serializeMessage(message));
}

function findFirstUserIndex(messages: Message[]): number {
	return messages.findIndex((message) => message.role === "user");
}

function findRecentStartIndex(messages: Message[]): number {
	let tokens = 0;
	for (let index = messages.length - 1; index >= 0; index -= 1) {
		const message = messages[index];
		if (!message) {
			continue;
		}
		tokens += estimateMessageTokens(message);
		if (tokens >= PRESERVE_RECENT_TOKENS) {
			return index;
		}
	}
	return 0;
}

function collectToolNames(messages: Message[]): string[] {
	const names = new Set<string>();
	for (const message of messages) {
		if (!Array.isArray(message.content)) {
			continue;
		}
		for (const block of message.content) {
			if (block.type === "tool_use" && block.name) {
				names.add(block.name);
			}
		}
	}
	return [...names].sort((left, right) => left.localeCompare(right));
}

function collectTouchedFiles(messages: Message[]): string[] {
	const paths = new Set<string>();
	for (const message of messages) {
		if (!Array.isArray(message.content)) {
			continue;
		}
		for (const block of message.content) {
			if (block.type === "file" && block.path) {
				paths.add(block.path);
			}
			if (block.type === "tool_use") {
				for (const value of Object.values(block.input ?? {})) {
					if (typeof value === "string" && value.includes("/")) {
						paths.add(value);
					}
				}
			}
		}
	}
	return [...paths].sort((left, right) => left.localeCompare(right));
}

// Spike 改造：生成时间戳 ID
function generateTimestampId(): string {
	const now = new Date();
	return now.toISOString().replace(/[:.]/g, "-");
}

// Spike 改造：写出 handoff.md
function writeHandoff(summary: string, tokensBefore: number, compactedCount: number): string {
	const timestampId = generateTimestampId();
	const timestamp = new Date().toISOString();
	const handoffContent = `# Handoff (Auto-generated by P5 Spike Plugin)

**Timestamp**: ${timestamp}
**Compact Event ID**: ${timestampId}
**Tokens Before Compaction**: ${tokensBefore}
**Messages Compacted**: ${compactedCount}

## Compact Summary

${summary}

## Files Mentioned (from compacted history)

See compact summary above.

---
*This handoff was auto-generated by the P5 Spike Plugin's registerMessageBuilder.*
`;

	const handoffDir = path.resolve(HANDOFF_DIR);
	if (!fs.existsSync(handoffDir)) {
		fs.mkdirSync(handoffDir, { recursive: true });
	}
	const handoffPath = path.join(handoffDir, HANDOFF_FILE);
	fs.writeFileSync(handoffPath, handoffContent, "utf-8");

	return timestampId;
}

// Spike 改造：追加 index.jsonl
function appendIndex(timestampId: string, tokensBefore: number, compactedCount: number, tools: string[], files: string[]): void {
	const indexEntry = {
		id: timestampId,
		timestamp: new Date().toISOString(),
		event: "compact",
		tokens_before: tokensBefore,
		messages_compacted: compactedCount,
		tools_used: tools,
		files_touched: files,
		handoff_file: `${HANDOFF_DIR}/${HANDOFF_FILE}`,
	};

	const handoffDir = path.resolve(HANDOFF_DIR);
	if (!fs.existsSync(handoffDir)) {
		fs.mkdirSync(handoffDir, { recursive: true });
	}
	const indexPath = path.join(handoffDir, INDEX_FILE);
	const line = JSON.stringify(indexEntry) + "\n";
	fs.appendFileSync(indexPath, line, "utf-8");
}

function buildCompactionSummary(
	compacted: Message[],
	tokensBefore: number,
): Message {
	const roleCounts = compacted.reduce<Record<string, number>>(
		(counts, message) => {
			counts[message.role] = (counts[message.role] ?? 0) + 1;
			return counts;
		},
		{},
	);
	const tools = collectToolNames(compacted);
	const files = collectTouchedFiles(compacted);
	const highlights = compacted
		.map(serializeMessage)
		.map((line) => preview(line, 500))
		.slice(-6);

	// Spike 改造：在 compact 触发时写出 handoff + index
	const summaryText = `Context summary:
## Compacted Range
- Messages compacted: ${compacted.length}
- Estimated tokens before compaction: ${tokensBefore}
- Roles: ${Object.entries(roleCounts)
		.map(([role, count]) => `${role}=${count}`)
		.join(", ")}
## Tool Activity
${tools.length > 0 ? tools.map((tool) => `- ${tool}`).join("\n") : "- none"}
## Files Mentioned
${files.length > 0 ? files.map((p) => `- ${p}`).join("\n") : "- none"}
## Recent Highlights From Compacted History
${highlights.length > 0 ? highlights.map((item) => `- ${item}`).join("\n") : "- none"}
Continue from this summary plus the preserved recent messages below.`;

	// Spike 改造：写出 handoff.md + 追加 index.jsonl
	try {
		const timestampId = writeHandoff(summaryText, tokensBefore, compacted.length);
		appendIndex(timestampId, tokensBefore, compacted.length, tools, files);
		console.log(`[p5-spike] Handoff written: ${HANDOFF_DIR}/${HANDOFF_FILE}`);
		console.log(`[p5-spike] Index appended: ${HANDOFF_DIR}/${INDEX_FILE}`);
	} catch (err) {
		console.error(`[p5-spike] Failed to write handoff/index:`, err);
	}

	return {
		role: "user",
		content: summaryText,
	};
}

export const plugin: AgentPlugin = {
	name: "p5-spike-compaction",
	manifest: {
		capabilities: ["messageBuilders", "hooks"],
	},
	setup(api) {
		api.registerMessageBuilder({
			name: "summarize-middle-history-with-handoff",
			build(messages) {
				const totalTokens = messages.reduce(
					(total, message) => total + estimateMessageTokens(message),
					0,
				);
				if (totalTokens < MAX_INPUT_TOKENS * COMPACT_AT_RATIO) {
					return messages;
				}
				const firstUserIndex = findFirstUserIndex(messages);
				const recentStartIndex = Math.max(
					firstUserIndex + 1,
					findRecentStartIndex(messages),
				);
				if (firstUserIndex < 0 || recentStartIndex <= firstUserIndex + 1) {
					return messages;
				}
				const prefix = messages.slice(0, firstUserIndex + 1);
				const compacted = messages.slice(firstUserIndex + 1, recentStartIndex);
				const recent = messages.slice(recentStartIndex);
				if (compacted.length === 0) {
					return messages;
				}
				console.log(`[p5-spike] Compaction triggered: ${totalTokens} tokens, ${compacted.length} messages to compact`);
				return [
					...prefix,
					buildCompactionSummary(compacted, totalTokens),
					...recent,
				];
			},
		});
	},
	// Spike 改造：#6 session_start hook 尝试
	// 官方 hook 处理器：beforeRun / afterRun / beforeModel / afterModel / beforeTool / afterTool / onEvent
	// session_start 对应的处理器未明确，尝试 beforeRun
	hooks: {
		beforeRun(context) {
			try {
				const timestamp = new Date().toISOString();
				const sessionLogDir = path.resolve(HANDOFF_DIR);
				if (!fs.existsSync(sessionLogDir)) {
					fs.mkdirSync(sessionLogDir, { recursive: true });
				}
				const sessionLogPath = path.join(sessionLogDir, "session-start.log");
				const logLine = `[${timestamp}] session_start hook fired (beforeRun)\n`;
				fs.appendFileSync(sessionLogPath, logLine, "utf-8");
				console.log(`[p5-spike] session_start hook fired, logged to ${HANDOFF_DIR}/session-start.log`);
			} catch (err) {
				console.error(`[p5-spike] session_start hook error:`, err);
			}
		},
	},
};

export default plugin;
